CREATE TABLE USERS (
	USER_NAME varchar(30) NOT NULL,
	PASSWORD varchar(30) NOT NULL,
	LOGGEDIN BOOL NULL,
	CONSTRAINT USERS_PK PRIMARY KEY (USER_NAME)
);

CREATE TABLE `SCHOOL_INFO` (
  `ID` int(11) NOT NULL AUTO_INCREMENT,
  `NAME` varchar(500) NOT NULL,
  `PROFILE` varchar(100) DEFAULT NULL,
  `ADDRESS` varchar(200) DEFAULT NULL,
  `DISTRICT` varchar(100) DEFAULT NULL,
  `PHONE1` varchar(30) DEFAULT NULL,
  `EMAIL` varchar(100) DEFAULT NULL,
  `PHONE2` varchar(30) DEFAULT NULL,
  `WEBSITE` varchar(100) DEFAULT NULL,
  `LOGO` varchar(200) DEFAULT NULL,
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=23 DEFAULT CHARSET=utf8;

CREATE TABLE `EVENT_INFO` (
  `ID` int(11) NOT NULL AUTO_INCREMENT,
  `TITLE` varchar(150) DEFAULT NULL,
  `DESCRIPTION` varchar(1000) DEFAULT NULL,
  `EVENT_DATE` date DEFAULT NULL,
  `LINK` varchar(200) DEFAULT NULL,
  PRIMARY KEY (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=19 DEFAULT CHARSET=utf8;

CREATE TABLE `STUDENT_INFO` (
  `ID` int(11) NOT NULL AUTO_INCREMENT,
  `NAME` varchar(200) DEFAULT NULL,
  `PHOTO` varchar(100) DEFAULT NULL,
  `SCHOOL_ID` int(11) NOT NULL,
  `PARENT_NAME` varchar(150) DEFAULT NULL,
  `ADDRESS1` varchar(150) DEFAULT NULL,
  `ADDRESS2` varchar(150) DEFAULT NULL,
  `ADDRESS3` varchar(150) DEFAULT NULL,
  `PHONE` varchar(30) DEFAULT NULL,
  `PHONE2` varchar(30) DEFAULT NULL,
  `EMAIL_ID` varchar(150) DEFAULT NULL,
  `CATEGORY` int(11) DEFAULT NULL,
  PRIMARY KEY (`ID`),
  KEY `STUDENT_INFO_SCHOOL_INFO_FK` (`SCHOOL_ID`),
  CONSTRAINT `STUDENT_INFO_SCHOOL_INFO_FK` FOREIGN KEY (`SCHOOL_ID`) REFERENCES `SCHOOL_INFO` (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=29 DEFAULT CHARSET=utf8;

CREATE TABLE `STUDENT_EVENTS` (
  `EVENT_ID` int(11) NOT NULL,
  `CATEGORY` int(11) DEFAULT NULL,
  `PRIZE_WONE` varchar(200) DEFAULT NULL,
  `ID` int(11) NOT NULL AUTO_INCREMENT,
  `STUDENT_ID` int(11) NOT NULL,
  PRIMARY KEY (`ID`),
  KEY `STUDENT_EVENTS_EVENT_INFO_FK` (`EVENT_ID`),
  KEY `STUDENT_EVENTS_SCHOOL_INFO_FK` (`STUDENT_ID`),
  CONSTRAINT `STUDENT_EVENTS_EVENT_INFO_FK` FOREIGN KEY (`EVENT_ID`) REFERENCES `EVENT_INFO` (`ID`),
  CONSTRAINT `STUDENT_EVENTS_STUDENT_INFO_FK` FOREIGN KEY (`STUDENT_ID`) REFERENCES `STUDENT_INFO` (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=28 DEFAULT CHARSET=utf8;

CREATE TABLE `EVENT_IMAGES` (
  `ID` int(11) NOT NULL AUTO_INCREMENT,
  `STUDENT_EVENT_ID` int(11) NOT NULL,
  `IMAGE_NAME` varchar(200) DEFAULT NULL,
  `IMAGE_DESCRIPTION` varchar(150) DEFAULT NULL,
  PRIMARY KEY (`ID`),
  KEY `EVENT_IMAGES_STUDENT_EVENTS_FK_1` (`STUDENT_EVENT_ID`),
  CONSTRAINT `EVENT_IMAGES_STUDENT_EVENTS_FK` FOREIGN KEY (`STUDENT_EVENT_ID`) REFERENCES `STUDENT_EVENTS` (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=50 DEFAULT CHARSET=utf8;